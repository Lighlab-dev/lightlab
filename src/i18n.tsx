const copyByLanguage = {
  ...translations,
  ar,
} as const

export type Language = keyof typeof copyByLanguage
export type Copy = typeof copyByLanguage[Language]

type LanguageContextValue = {
  language: Language
  setLanguage: (next: Language) => void
  copy: Copy
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    if (typeof window === 'undefined') {
      return 'fr'
    }
    const stored = localStorage.getItem('lightlab_language')
    return stored === 'en' || stored === 'fr' || stored === 'ar' ? stored : 'fr'
  })

  useEffect(() => {
    if (typeof window === 'undefined') return
    localStorage.setItem('lightlab_language', language)
    document.documentElement.setAttribute('lang', language)
    document.documentElement.setAttribute('dir', language === 'ar' ? 'rtl' : 'ltr')
  }, [language])

  const value = useMemo(() => ({ language, setLanguage, copy: copyByLanguage[language] }), [language])

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider')
  }
  return context
}
